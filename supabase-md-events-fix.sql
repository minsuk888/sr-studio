-- ============================================
-- MD 이벤트 수정: R9 삭제 + R6 온라인 판매 전환
-- 2026 시즌은 총 8라운드 (R1~R8)
-- 실행: Supabase Dashboard > SQL Editor
-- ============================================

-- ─── 1. R9 삭제 (9라운드는 없음) ────────────────────────────────────────────────
DELETE FROM md_events WHERE name = 'R9';

-- ─── 2. R6 전남GT → 온라인 판매로 전환 ─────────────────────────────────────────
UPDATE md_events
SET name = 'R6 전남GT (온라인)',
    memo = '현장판매 제외, 온라인 판매로 전환'
WHERE name = 'R6 전남GT';

